const fontkit = require('fontkit');
const path = require('path');
const fs = require('fs');

const FONT_PATH = path.resolve(__dirname, 'fonts/Montserrat-ExtraBold.ttf');
const TEXT = 'TermBeam';
const FONT_SIZE = 100;

const font = fontkit.openSync(FONT_PATH);
const scale = FONT_SIZE / font.unitsPerEm;
const baselineY = FONT_SIZE;

const paths = [];
let cursorX = 0;

for (const ch of TEXT) {
  const cp = ch.codePointAt(0);
  const glyph = font.glyphForCodePoint(cp);
  const p = glyph.path
    .scale(scale, -scale)
    .translate(cursorX, baselineY);
  const advance = glyph.advanceWidth * scale;
  paths.push({ ch, d: p.toSVG(), advance });
  cursorX += advance;
}

const out = [];
out.push(`// Auto-generated by tools/extract-wordmark-paths.cjs`);
out.push(`// Source: Montserrat ExtraBold (TTF), fontSize ${FONT_SIZE}`);
out.push(`// Wordmark: ${TEXT}, natural width: ${cursorX.toFixed(2)}`);
out.push(``);
out.push(`export interface WordmarkPath { ch: string; d: string }`);
out.push(``);
out.push(`export const WORDMARK_PATHS: WordmarkPath[] = [`);
for (const p of paths) {
  out.push(`  { ch: '${p.ch}', d: '${p.d}' },`);
}
out.push(`];`);
out.push(``);
out.push(`/** SVG viewBox sized to the wordmark's natural extents. */`);
out.push(`export const WORDMARK_VIEW_BOX = '0 0 ${Math.ceil(cursorX)} ${FONT_SIZE * 1.2}';`);
out.push(`export const WORDMARK_WIDTH = ${Math.ceil(cursorX)};`);
out.push(`export const WORDMARK_HEIGHT = ${FONT_SIZE * 1.2};`);
out.push(``);
fs.writeFileSync(
  path.resolve(__dirname, '../src/components/common/wordmarkPaths.ts'),
  out.join('\n'),
);
console.log(`Wrote ${paths.length} letter paths, width ${cursorX.toFixed(2)}`);
